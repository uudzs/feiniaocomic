<?php

declare(strict_types=1);
// ========================================================================
// 权限角色组验证器
// ========================================================================
namespace app\common\validate\manage;

use think\Validate;

class AuthGroup extends Validate
{
    //----------------------------------------------------------------------
    // 验证规则
    //----------------------------------------------------------------------
    protected $rule = [
        'title' => 'require|max:60', // 角色名称必填且最长60字
    ];

    //----------------------------------------------------------------------
    // 错误提示
    //----------------------------------------------------------------------
    protected $message = [
        'title.require' => '标题必填',
        'title.max'     => '标题最多不能超过60个字',
    ];

    //----------------------------------------------------------------------
    // 验证场景
    //----------------------------------------------------------------------
    protected $scene = [
        'add'  => ['title'],  // 新增场景验证字段
        'edit' => ['title'],  // 编辑场景验证字段
    ];
}
