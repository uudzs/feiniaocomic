<?php

declare(strict_types=1);

namespace app\common\validate\manage;

use think\Validate;

class Link extends Validate
{
    //──────────────────────────────────────────────────
    // 验证规则配置
    //──────────────────────────────────────────────────
    protected $rule = [
        'title' => 'require|max:60',  // 标题必填且最大60字符
        'thumb' => 'max:250',         // 缩略图地址最大250字符
        'url'   => 'max:250|url', // 链接需为有效完整URL
        'desc'  => 'max:250',         // 描述信息最大250字符
    ];

    //──────────────────────────────────────────────────
    // 验证失败提示信息
    //──────────────────────────────────────────────────
    protected $message = [
        'title.require' => '标题不得为空',
        'title.max'     => '标题长度不得超过60个字符',
        'thumb.max'     => '图片地址长度不得超过250个字符',
        'url.max'       => '链接地址长度不得超过250个字符',
        'url.url' => '链接地址必须是有效的完整网址（包含http://或https://）',
        'desc.max'      => '描述内容长度不得超过250个字符',
    ];

    //──────────────────────────────────────────────────
    // 验证场景配置
    //──────────────────────────────────────────────────
    protected $scene = [
        'add',  // 添加链接场景
        'edit'  // 编辑链接场景
    ];
}
